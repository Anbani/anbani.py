_rng = random.Random()
_dataset = VEFXWORDS


def seed(n):
    """Seed the module RNG for reproducible output (testing aid)."""
    _rng.seed(n)


def load_wordlist(wordlist):
    """Swap the active word source used by ``sentences``/``random_word``."""
    global _dataset
    _dataset = wordlist


def _random_number(lo, hi):
    # Integer in [lo, hi), mirroring the JS Math.floor(random()*(hi-lo)+lo).
    return int(_rng.random() * (hi - lo) + lo)


def _set_char_at(s, where, what, offset=0):
    # Pure port of the JS String.prototype.setCharAt helper.
    if where > len(s) - 1:
        return s
    return s[:where] + what + s[where + len(what) + offset:]


def _random_mark():
    r = _rng.random()
    for mark, threshold in _FREQ_SUMMED:
        if r < threshold:
            return mark + " "
    return " "


def random_word():
    return _dataset[_random_number(0, len(_dataset))]


def random_first_name():
    return FNAMES[_random_number(0, len(FNAMES))]


def random_last_name():
    return LNAMES[_random_number(0, len(LNAMES))]


def _create_words(nwords):
    return [random_word() for _ in range(nwords)]


def _create_sentences(words):
    s = ""
    for w in words:
        s += w + _random_mark()
    s = s.strip()
    s = _set_char_at(s, len(s) - 1, ".", 1)
    return s


def sentences(nwords):
    """One fake sentence of ``nwords`` words, terminated with a period."""
    return _create_sentences(_create_words(nwords))


def paragraphs(nwords, npar, newline="\n\n"):
    """``npar`` paragraphs, each of ``nwords`` words; trailing newline kept."""
    out = ""
    for _ in range(npar):
        out += _create_sentences(_create_words(nwords)) + newline
    return out


def names(nnames):
    """List of ``nnames`` "First Last" strings."""
    return [random_first_name() + " " + random_last_name() for _ in range(nnames)]
